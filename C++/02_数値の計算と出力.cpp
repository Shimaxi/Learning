#include <iostream>
using namespace std;

int main()
{
    cout << 10 << "\n";
    cout << "10 + 5 = " << 10 + 5 << "\n";
    cout << "10 - 5 = " << 10 - 5 << "\n";
    cout << "10 * 5 = " << 10 * 5 << "\n";
    cout << "10 / 5 = " << 10 / 5 << "\n";
    cout << "10 % 5 = " << 10 % 5 << "\n";
    return 0;
}
